package com.calculadora.domain;

public class Calculadora {
    public int soma(int x, int y) {
        return x + y;
    }

    public int subtracão(int x, int y) {
        return x - y;
    }

    public int multiplicacão(int x, int y) {
        return x * y;
    }
    public double divisao(double x, double y) {
        return x / y;
    }
}
